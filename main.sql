DROP DATABASE IF EXISTS Efemerydy;
CREATE DATABASE IF NOT EXISTS Efemerydy;
USE Efemerydy;

CREATE TABLE Sun(
  id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  longt DECIMAL(10,7) NOT NULL,
  distAU DECIMAL(11,9) NOT NULL
);

CREATE TABLE Dates (
  id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  dates CHAR(11) NOT NULL
);

DROP TABLE General;

CREATE TABLE General (
 id SMALLINT AUTO_INCREMENT PRIMARY KEY,
 planet VARCHAR (20) NOT NULL UNIQUE ,
 max_id INT UNSIGNED NOT NULL,
 min_AU DECIMAL(11,9) NOT NULL,
 max_AU DECIMAL(11,9) NOT NULL,
 avg_AU DECIMAL(11,9) NOT NULL
);

